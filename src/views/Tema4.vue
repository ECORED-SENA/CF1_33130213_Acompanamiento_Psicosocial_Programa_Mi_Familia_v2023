          p Las fortalezas y recursos de las familias deben ser reconocidas y apropiadas por parte de sus integrantes, este reconocimiento y apropiación permite fortalecer las capacidades familiares; lo anterior se facilita a través del acompañamiento psicosocial, el cual dispone escenarios de diálogo y reflexión frente a las creencias, prácticas y actitudes familiares, de tal manera que se consoliden o se transformen en respuesta a su potencial para promover el desarrollo individual y colectivo.


