          h4 Enfoque de curso de vida

          p El enfoque de curso de vida concibe el desarrollo humano como un proceso continuo a lo largo de la vida y reconoce la importancia de los entornos en los que los sujetos interactúan. En este enfoque se analizan la trayectoria y las transiciones en la vida individual y familiar, reconociendo el impacto que tiene las condiciones históricas, sociales, culturales, biológicas y psicológicas (Ministerio de Salud y Protección Social, 2018).

          p La trayectoria hace referencia a la ruta de la vida de los sujetos, que puede variar y cambiar en dirección, grado y proporción (Elder, 1991). Mientras que la transición responde a los diferentes episodios en que se desagrega la trayectoria, no necesariamente predefinidos, pero que conllevan a cambios de estado, posición o situación del individuo al interior de su familia y comunidad (Sepúlveda, 2010).

          p Este enfoque destaca que la trayectoria no tiene una secuencia particular ni velocidad determinada, así como las transiciones no son fijas y se pueden presentar en cualquier momento de la vida (Blanco, 2011).

